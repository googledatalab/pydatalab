  def test_repeating_data(self):
    schema = [{'name': 'counts', 'type': 'INTEGER', 'mode': 'REPEATED'}]
    data = {'f': [{'v': [{'v': 0}, {'v': 1}, {'v': 2}]}]}
    parsed = {'counts': [0, 1, 2]}
    result = bq._parser.Parser.parse_row(schema, data)
    self.assertEqual(parsed, result)


